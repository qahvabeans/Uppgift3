package mediacollector;

public class Serie {

    private String serieTjanst;
    private String serieNamn;
    private String serieBetyg;

    public String getSerieTjanst() {
        return serieTjanst;
    }

    public void setSerieTjanst(String serieTjanst) {
        this.serieTjanst = serieTjanst;
    }

    public String getSerieNamn() {
        return serieNamn;
    }

    public void setSerieNamn(String serieNamn) {
        this.serieNamn = serieNamn;
    }

    public String getSerieBetyg() {
        return serieBetyg;
    }

    public void setSerieBetyg(String serieBetyg) {
        this.serieBetyg = serieBetyg;
    }
}
